//----------------------------------------------------------------------------------------------------
//загрузка числа типа long
//----------------------------------------------------------------------------------------------------
long LoadLong(FILE *file)
{
 long value;
 fread(&value,1,sizeof(long),file);
 return(value);
}
//----------------------------------------------------------------------------------------------------
//запись числа типа long
//----------------------------------------------------------------------------------------------------
void SaveLong(FILE *file,long value)
{
 fwrite(&value,1,sizeof(long),file); 
}
